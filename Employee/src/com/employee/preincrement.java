package com.employee;

public class preincrement {
	public static void main(String[] args) {
		int i=12;
  
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
        System.out.println(++i);
        System.out.println(i);
        System.out.println(i++);
        
		System.out.println(i++ + ++i);

        	}

}
